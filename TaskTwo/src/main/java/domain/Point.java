package domain;

/** Class position point
 *
 * @author Samoylenko Victor
 * @version 1.0
 */

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
